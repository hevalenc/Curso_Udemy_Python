#aula sobre operadores aritméticos
#// - divisão inteira (resultado arredondado), ** - potenciação, % - resto da divisão

print('Multiplicação *: ', 10 * 10)
print('Adição +: ', 10 + 10)
print('Subtração -: ', 10 - 10)
print('Divisão /: ', 10 / 3)
print('Divisão inteira //: ', 10 // 3)
print('Potenciação **: ', 2 ** 3)
print('Resto da divisão %: ', 10 % 2, 10 % 3)
print('Operação com parênteses: ', (5 + 2) * 10, 5 + 2 * 10)

#o sinal de multiplicação pode ser usado como repetidor, use 'inteiro' * 'string'
print('Repetição: ', 10 * 'OK')

#o sinal de adição pode ser usado para concatenar 'strings', mas não é comum
print('Heitor' + ' ' + 'Valença')